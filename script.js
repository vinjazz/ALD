    // Verifica CORS prima di usare l'eraser
    function checkCORSBeforeErasing(imageObj) {
        if (!imageObj || imageObj.type !== 'image') return false;
        
        // Se abbiamo gi verificato, usa il risultato memorizzato
        if (imageObj.corsEnabled !== undefined) {
            return imageObj.corsEnabled;
        }
        
        try {
            const testCanvas = document.createElement('canvas');
            const testCtx = testCanvas.getContext('2d');
            const imgElement = imageObj.getElement();
            
            testCanvas.width = 1;
            testCanvas.height = 1;
            testCtx.drawImage(imgElement, 0, 0, 1, 1);
            testCtx.getImageData(0, 0, 1, 1);
            
            imageObj.corsEnabled = true;
            return true;
        } catch (error) {
            imageObj.corsEnabled = false;
            return false;
        }
    }

    // Preload delle immagini con gestione CORS e file protocol
    function preloadImageWithCORS(src) {
        return new Promise((resolve, reject) => {
            // Se siamo su file://, rifiuta immediatamente
            if (isFileProtocol()) {
                reject(new Error('File protocol not supported for remote images'));
                return;
            }

            const img = new Image();
            img.crossOrigin = 'anonymous';
            
            img.onload = () => {
                try {
                    const testCanvas = document.createElement('canvas');
                    testCanvas.width = 1;
                    testCanvas.height = 1;
                    const testCtx = testCanvas.getContext('2d');
                    testCtx.drawImage(img, 0, 0, 1, 1);
                    testCtx.getImageData(0, 0, 1, 1);
                    
                    resolve({ img, corsEnabled: true });
                } catch (error) {
                    resolve({ img, corsEnabled: false });
                }
            };
            
            img.onerror = () => {
                // Retry senza CORS
                const img2 = new Image();
                img2.onload = () => resolve({ img: img2, corsEnabled: false });
                img2.onerror = () => reject(new Error('Failed to load image'));
                img2.src = src;
            };
            
            img.src = src;
        });
    }
    
